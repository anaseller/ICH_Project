# 1. Напишите программу, которая принимает матрицу (вложенный список) от пользователя
# и находит сумму всех элементов в матрице. Используйте вложенные списки и циклы для обхода элементов матрицы.
#
# Пример матрицы: [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
# Пример вывода:
# Сумма элементов в матрице: 45


matrix = []
rows = int(input("Введите количество строк в матрице: "))

for i in range(rows):
    row = list(map(int, input(f"Введите элементы строки {i+1} через пробел: ").split()))
    matrix.append(row)

total_sum = 0
for row in matrix:
    for element in row:
        total_sum += element

print("Сумма элементов в матрице:", total_sum)

